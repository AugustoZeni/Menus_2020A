    private void limparCampos() {
        txtCodigo.setText("");
        txtNome.setText("");
        txtTelefone.setText("");
        txtCidade.setText("");
        txtCodigo.grabFocus();
    }
    
    
    
    
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        txtCodigo.grabFocus();
        
        File dados = new File("dados.db"); // Banco de Dados Sqlite
        if (dados.exists()) {
            try {
                con = DriverManager.getConnection("jdbc:sqlite:dados.db");   
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Arquivo de Dados não encontrado");
            dispose();
        }
    }//GEN-LAST:event_formWindowOpened

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if ((txtCodigo.getText().trim().length() > 0) && 
                (txtNome.getText().trim().length() > 0) ) {
            
            // Adicionar
            DefaultTableModel dados = (DefaultTableModel)tblClientes.getModel(); // Cast
            String linhaNova[] = {txtCodigo.getText(), txtNome.getText(), txtTelefone.getText(), txtCidade.getText()};
            dados.addRow(linhaNova);
            
            // Se tiver marcada, remove
            int linha = tblClientes.getSelectedRow();
            if (linha >= 0) {
                dados.removeRow(linha);
            }

            // Atualiza o Model            
            tblClientes.setModel(dados);  
            
            try {
                // Grava no Banco
                PreparedStatement ps = con.prepareStatement("INSERT INTO clientes (nome, telefone, cidade) VALUES (?, ?, ?)");
                ps.setString(1, txtNome.getText());
                ps.setString(2, txtTelefone.getText());
                ps.setString(3, txtCidade.getText());
                ps.execute();
                ps.close();               
                JOptionPane.showMessageDialog(null, "Cliente inserido com sucesso!");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao inserir o cliente!");
            }
            
            
            limparCampos();
            
        } else {
            JOptionPane.showMessageDialog(this, "Código ou Nome não informado!");
        }
      
        
        
        
        
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        int linha = tblClientes.getSelectedRow();
        if (linha >= 0) {
            DefaultTableModel model = (DefaultTableModel) tblClientes.getModel();
            model.removeRow(linha);
            tblClientes.setModel(model);
        } else {
            JOptionPane.showMessageDialog(this, "Você precisa escolher 1 linha para excluir");            
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

