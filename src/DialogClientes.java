    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        // TODO add your handling code here:
        int linha = tblClientes.getSelectedRow();
        if (linha >= 0) {
            txtCodigo.setText(tblClientes.getValueAt(linha, 0).toString());
            txtNome.setText(tblClientes.getValueAt(linha, 1).toString());
            txtTelefone.setText(tblClientes.getValueAt(linha, 2).toString());
            txtCidade.setText(tblClientes.getValueAt(linha, 3).toString());
        }
    }//GEN-LAST:event_tblClientesMouseClicked

